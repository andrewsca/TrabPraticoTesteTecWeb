<p>Como economizar Energia Elétrica

Economizar energia elétrica é utilizá-la de forma a obter o máximo benefício com um menor consumo de energia, evitando os desperdícios ou o uso inadequado, sem no entanto, diminuir a qualidade, o conforto e a segurança.

Por que Economizar ?

Os custos crescentes, a escassez de recursos, a baixa remuneração, a disponibilidade de recursos hídricos, a otimização dos investimentos e os grandes desperdícios, fazem que seja importante a economia de energia elétrica para o nosso País e acarreta uma menor Nota Fiscal de Energia Elétrica a ser paga pelo consumidor. A economia de energia elétrica é uma fonte virtual de geração de energia elétrica, pois a energia deixa de ser desperdiçada. O custo do kWh economizado é cerca de 6 vezes mais barato do que o kWh gerado e ainda não agride ao meio ambiente.</p>
	
